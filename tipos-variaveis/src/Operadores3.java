public class Operadores3 {
    public static void main(String[] args){
        int numero = 5;
        System.out.println( numero);
        System.out.println(- numero);
        numero = -numero;
        System.out.println( numero);
        System.out.println(- numero);
        numero = numero * -1;
        System.out.println( numero);
        System.out.println(- numero);
    }
}
