public class Operadores4 {
    public static void main(String[] args){
    
        int numero = 5;
        //x repetição
        numero = numero + 1;
        System.out.println(numero);
        numero++;
        System.out.println(numero);
        System.out.println(numero++);
        System.out.println(numero);
        System.out.println(++numero);
        System.out.println(numero);
        System.out.println(numero--);
        System.out.println(numero);
        System.out.println(--numero);
        System.out.println(numero);
    }
}
